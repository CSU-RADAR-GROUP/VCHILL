package edu.colostate.vchill.chill;

/**
 * Polarization mode used by transmitter
 * @author Jochen Deyke
 * @version 2007-09-19
 */
public enum PolarizationMode
{
    /** Vertical only */
    V,
    
    /** Horizontal only */
    H,

    /** Alternating vertical and horizontal */
    VH,
    
    /** Simultaneous vertical and horizontal */
    VHS,
}
